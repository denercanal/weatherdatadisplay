    private void incluirDadosActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_incluirDadosActionPerformed
        JSONObject jsonObject = new JSONObject();
        jsonObject.put("Data", ultimaData.getText());
        jsonObject.put("Temperatura", ultimaTemperatura.getText());
        jsonObject.put("Umidade", ultimaUmidade.getText());
        jsonObject.put("Pressão", ultimaPressao.getText());
        jsonObject.put("Tipo", "Inclusão");
        String xml = XML.toString(jsonObject);
        DadosLog.getJsonArray().add(jsonObject);
        DadosLog.getXmlArray().append("<log>").append(xml).append("</log>");
        int total = Integer.parseInt(totalDeRegistros.getText());
        total++;
        totalDeRegistros.setText(Integer.toString(total));
    }// GEN-LAST:event_incluirDadosActionPerformed

