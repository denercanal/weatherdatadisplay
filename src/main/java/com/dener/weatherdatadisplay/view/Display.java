    private void salvarTipoActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_salvarTipoActionPerformed
        try {
            if (configSistemaLog.getSelectedIndex() == 0) {
                FileWriter file = new FileWriter("/home/dener/NetBeansProjects/weatherdatadisplay/src/main/java/com/dener/weatherdatadisplay/log/output.json");
                file.write(DadosLog.getJsonArray().toJSONString());
                file.close();
            }
            if (configSistemaLog.getSelectedIndex() == 1) {
                FileWriter file = new FileWriter("/home/dener/NetBeansProjects/weatherdatadisplay/src/main/java/com/dener/weatherdatadisplay/log/output.xml");
                file.write(DadosLog.getXmlArray().toString());
                file.close();
            }
        } catch (IOException e) {
            e.printStackTrace();
        }
    }// GEN-LAST:event_salvarTipoActionPerformed

